class tv{
    constructor(channel,volume,status){
        this.channel=channel;
        this.volume=volume;
        this.status=status;
    }
    setchannel(newchannel){
        this.channel=newchannel;
        
    }
    setvolume(newvolume){
        this.volume=newvolume;
    }
    setstatus(newstatus){
        this.status=newstatus;
    }
}
